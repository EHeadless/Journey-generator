/**
 * Which input mix produced a given hypothesis variant. Drives the badge
 * on the variant comparison view and the prompt context blend.
 *
 * - `form-only`     → only the structured brief form
 * - `brief-only`    → only the uploaded brief document
 * - `research-only` → only the uploaded research documents
 * - `form+research` → form + research documents (no brief doc)
 * - `everything`    → form + brief doc + research documents
 */
export type HypothesisBlend =
  | 'form-only'
  | 'brief-only'
  | 'research-only'
  | 'form+research'
  | 'everything';

/**
 * One generated hypothesis landscape variant. The store keeps several of
 * these side by side so the user can compare them; the `isActive` flag
 * marks the one mirrored onto `Model.journeyPhases` for downstream
 * compatibility (existing demand-space / circumstance generation only
 * knows about journeyPhases). `sourceFingerprint` captures a hash of the
 * inputs that produced the variant so we can detect staleness.
 *
 * INVARIANT — "only one landscape is live": when a variant in either
 * `hypothesisVariants` or `informedVariants` is set active, all variants
 * in BOTH arrays must be marked inactive except for the one being
 * activated. The active variant's phases mirror to `Model.journeyPhases`.
 */
export interface HypothesisVariant {
  id: string;
  blend: HypothesisBlend;
  label: string;                      // Display label, e.g. "Form + Research"
  journeyPhases: JourneyPhase[];      // Phases generated for this blend
  generatedAt: Date;
  tokenCost?: number;
  sourceFingerprint: string;
  isActive: boolean;
}

/**
 * Which evidence mix produced a given Informed Landscape variant.
 * Anchored on classified problems (mandatory) plus optional supporting
 * sources.
 *
 * - `problems-only`     → classified problems alone
 * - `problems+brief`    → problems + verbatim client brief
 * - `problems+research` → problems + research summaries
 * - `everything`        → problems + brief + research + form fields
 */
export type InformedBlend =
  | 'problems-only'
  | 'problems+brief'
  | 'problems+research'
  | 'everything';

/**
 * One Informed-landscape variant. Parallel to HypothesisVariant, anchored
 * on classified problems instead of brief/research.
 *
 * INVARIANT — "only one landscape is live": when a variant in either
 * `hypothesisVariants` or `informedVariants` is set active, all other
 * variants in BOTH arrays must be marked inactive. The active variant's
 * phases mirror to `Model.journeyPhases` so the existing demand-space /
 * circumstance cascade keeps working unchanged.
 */
export interface InformedLandscapeVariant {
  id: string;
  blend: InformedBlend;
  label: string;
  journeyPhases: JourneyPhase[];
  generatedAt: Date;
  tokenCost?: number;
  sourceFingerprint: string;
  isActive: boolean;
}

export interface Model {
  id: string;
  input: ModelInput;
  /**
   * Top-level journeys on this model (Arrival / Transit / Departure, etc.).
   * Always present on newly created models. Older models (from before
   * multi-journey support landed) may be missing this field on disk;
   * the store rehydration migrates them into a single default journey.
   */
  journeys: Journey[];
  journeyPhases: JourneyPhase[];
  demandSpaces: DemandSpace[];
  /**
   * Cross-cutting demand spaces — JTBDs that address systemic / cross-
   * cutting problems (those with `affectedPhaseIds: []` in the diagnostics).
   * They do NOT belong to any journey phase and are rendered in their own
   * silo on the Informed Landscape route. Generated by
   * `/api/generate-cross-cutting-demand-spaces`. Optional for backwards
   * compatibility; the store rehydration migration defaults to `[]`.
   */
  crossCuttingDemandSpaces?: DemandSpace[];
  circumstances: Circumstance[]; // 5 composite circumstances per demand space
  marketingActivations: MarketingActivation[];
  productActivations: ProductActivation[];
  serviceActivations: ServiceActivation[];
  personaMappings: PersonaMapping[]; // Persona filtering

  /**
   * All side-by-side hypothesis landscape variants generated for this
   * model. The active one's phases are mirrored to `journeyPhases` so the
   * downstream demand-space / circumstance pipeline keeps working
   * unchanged.
   */
  hypothesisVariants?: HypothesisVariant[];

  /**
   * All side-by-side Informed Landscape variants generated for this
   * model. Built from classified problems (Problem Diagnostics step).
   * The active one's phases are mirrored to `journeyPhases`. See
   * InformedLandscapeVariant doc for the bidirectional active-landscape
   * invariant with `hypothesisVariants`.
   */
  informedVariants?: InformedLandscapeVariant[];

  // Discovery & Signal Mapping (Phase 1)
  discoveryPlan?: DiscoveryPlan;
  evidenceRecords?: Evidence[];
  signals?: Signal[];
  mappings?: Mapping[];
  proposals?: Proposal[];
  discoveryBundle?: DiscoveryBundle;
  currentStep?: ConsultancyStep;

  // Workshop inventory (Plan step)
  workshops?: Workshop[];
  workshopQuestions?: WorkshopQuestion[];

  // Capture-flow gating: hard-locks Findings + Diagnostics tabs until the
  // user has either confirmed Q&A (acknowledging that any edits are
  // baked in) or explicitly skipped it. Exactly one of the two should be
  // set at a time; calling skip clears confirm and vice versa.
  qaConfirmedAt?: Date | null;
  qaSkippedAt?: Date | null;

  // NOTE: Capture & Extraction entities (Upload, TranscriptChunk,
  // Extraction, QuestionAnswer, ExtractedSignal) live in IndexedDB only,
  // not on Model. They're too heavy for localStorage (parsed transcripts
  // + embeddings) and have real relational shape. The store exposes them
  // as a parallel `capture` slice hydrated via the storage adapter.
  // See lib/storage/* and the capture slice in lib/store.ts.

  createdAt: Date;
  updatedAt: Date;
}

