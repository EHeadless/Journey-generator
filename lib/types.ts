/**
 * A full brief document the user uploads on the home page (PDF/Word/etc).
 * Parsed text is retained verbatim so downstream prompts can pass the
 * complete context — not just the form fields the user pre-filled from it.
 */
export interface BriefDocument {
  filename: string;
  text: string;            // Parsed plain text (no truncation)
  uploadedAt: Date;
  sizeBytes: number;
  charCount: number;
}

/**
 * A research artifact uploaded in the optional Research step (between
 * Brief and Hypothesis Landscape). Multiple documents are allowed.
 * Each carries an LLM-generated summary so we can compose contextual
 * blends without re-paying for full-text tokens on every downstream call.
 */
/** Structured distillation of a single research artifact. Generated by
 *  /api/summarize-research-doc and stored alongside the verbatim text on
 *  the ResearchDocument so downstream context blends can inject the
 *  substance without paying full-text token costs on every prompt. */
export interface ResearchDocSummary {
  headline?: string;
  evidenceType?: string;
  keyFindings?: string[];
  namedSegments?: string[];
  namedJourneys?: string[];
  painsAndFrictions?: string[];
  opportunitiesOrHypotheses?: string[];
  directQuotes?: string[];
  summary?: string;          // 80-150 word prose distillation (this is what gets injected into prompts)
}

export interface ResearchDocument {
  id: string;
  filename: string;
  text: string;              // Parsed plain text (no truncation)
  summary?: ResearchDocSummary;  // Structured distillation; summary.summary is the injectable prose
  summarizedAt?: Date;
  uploadedAt: Date;
  sizeBytes: number;
}

