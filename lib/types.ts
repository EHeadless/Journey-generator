// Core types for Journey Generator

export type ExperienceType = 'product' | 'marketing' | 'service';

/**
 * A top-level journey on a model. A model can contain several journeys
 * (for example an airport model has Arrival, Transit, Departure) and each
 * journey has its own Pre-discovery JTBD blueprint — a one-liner that frames what the
 * customer is fundamentally trying to do during that journey. Journey
 * phases, demand spaces, dimensions, and activations are all scoped to a
 * single journey via `JourneyPhase.journeyId`.
 */
export interface Journey {
  id: string;
  name: string;
  jtbdBlueprint: string;
  order: number;
}

export interface JourneyPhase {
  id: string;
  /**
   * The journey this phase belongs to. Optional at the type level only to
   * keep backwards compatibility with pre-multi-journey models loaded
   * from localStorage; the store's migration stamps legacy phases on
   * hydration so in practice every phase in the store has a journeyId.
   */
  journeyId?: string;
  label: string;
  description: string;
  trigger: string; // What event marks entry into this phase
  order: number;
  /**
   * Where this phase came from.
   * - `user`      → user typed or preloaded the phase in the brief (e.g. Dubai Airport example)
   * - `ai`        → generated by the journey-phases endpoint
   * - `discovery` → proposed from discovery evidence/signals
   * Optional for backwards compatibility with older models.
   */
  source?: 'user' | 'ai' | 'discovery';
  /**
   * Which inputs the phase was generated from. Used for badging on the
   * Hypothesis Landscape variant comparison.
   * - `form-only`       → only the structured brief form (industry, business
   *                       description, painPoints…)
   * - `brief-only`      → form + uploaded brief document
   * - `research-backed` → form + uploaded research documents (no brief doc)
   * - `mixed`           → both brief doc and research docs
   * - `signal-backed`   → discovery evidence (post-Capture)
   */
  evidence?: 'form-only' | 'brief-only' | 'research-backed' | 'mixed' | 'signal-backed';
}

export interface DemandSpace {
  id: string;
  /**
   * For phase-scoped demand spaces (default), the FK into JourneyPhase.
   * Cross-cutting demand spaces (`scope === 'cross-cutting'`) carry the
   * sentinel `''` here — they don't belong to any phase. The store keeps
   * cross-cutting demand spaces in `Model.crossCuttingDemandSpaces` so
   * existing per-phase grouping logic stays untouched.
   */
  journeyPhaseId: string;
  label: string; // 2-4 words
  jobToBeDone: string; // "When I [situation], I want to [action], so that [outcome]"
  description?: string; // Optional 2-3 sentences (kept for backwards compatibility)
  order: number;
  /**
   * IDs of `ProblemDiagnostic` rows this JTBD addresses. Populated only
   * when the demand space was generated on the Informed Landscape route
   * (the route passes classified problems into the prompt and the LLM
   * tags each JTBD with the problems it covers). Absent/empty on
   * Hypothesis Landscape demand spaces and pre-provenance demand spaces.
   */
  sourceProblemIds?: string[];
  /**
   * Whether this demand space lives under a journey phase or sits in the
   * cross-cutting silo. Defaults to `'phase'` when absent (backwards
   * compatibility).
   */
  scope?: 'phase' | 'cross-cutting';
}

/**
 * A Circumstance is a composite position across the 5 universal axes
 * (Knowledge, Intent, Composition, Constraint, Moment) plus a narrative
 * JTBD-formatted statement and the Struggle/Progress forces. Each demand
 * space gets exactly 5 Circumstances, and collectively they must cover
 * contrast on every axis (e.g. at least one Novice + one Expert).
 */
export interface Circumstance {
  id: string;
  demandSpaceId: string;
  order: number;
  // Axis tuple — one value per axis
  knowledge: string;     // e.g. "Expert"
  intent: string;        // e.g. "Routine"
  composition: string;   // e.g. "Solo"
  constraint: string;    // e.g. "Time"
  moment: string;        // e.g. "Long-haul business travel"
  // Narrative (assembled as "When I am [context], I want to [action], so that [outcome].")
  context: string;       // "flying alone on a long-haul overnight work trip and already know what content unwinds me"
  action: string;        // "quickly access familiar entertainment options"
  outcome: string;       // "relax fast and make the journey feel shorter"
  // Forces framework
  struggle: string;      // "The system slows me down when I already know what I want."
  progress: string;      // "Get into rest mode quickly and reduce the mental drag of travel."
  // Optional provenance (matches existing signal-backed pattern)
  supportingSignalIds?: string[];
  evidence?: 'signal-backed' | 'brief-only';
}

